          <h1>22. Martabak Manis. </h1>
          <p>Pada saat pertama kali membuatnya aku gagal, sedih banget terus mikir apa yang salah, di resep pakai santan sebagai cairan utama adukan, namun akhirnya aku ganti pakai air biasa karena logikanya adonan bakal lebih light dan muncul rongganya. Eh bener dan berhasil masak pake frypan biasa. </p>
