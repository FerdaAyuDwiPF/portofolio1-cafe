          <h1>12. Kulit Dimsum. </h1>
          <p>Sangat penting untuk selalu sedia semangkuk tepung tapioka saat mengadon, jangan lupa rolling pin dikasih tepung juga. Pakai tepung protein tinggi ya. Kalo saya memutuskan lebih suka membuat kulit dadakan saat isiannya sudah siap. </p>
