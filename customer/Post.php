          <h1>18. Soes. </h1>
          <p>Saat diaduk di panci liat konsistensi kekentalannya. Adonannya harus kental menempel pas diaduk di api yang telah menyala jadi kalo masih terlalu tercium aroma butter tambah tepungnya.</p>
