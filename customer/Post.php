          <h1>8. Boba. </h1>
          <p>Ngulen adonan sampai kalis ternyata susah karena dasarnya tapioka, jadi pasti lengket. Airnya harus ditambah sedikit demi sedikit. Tipsnya lebih ke penyimpanan (Jangan simpan boba matang di kulkas!)</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIbz6U4AAdp9q.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIcQiUMAMHyRa.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIdBZU4AA1dVq.jpg" alt="">
          <h1>9. Ikan Tepung. </h1>
          <p>Kalau bingung masak ikan yang teksturnya gampang hancur, bisa diakali dengan diaduk pake tepung terigu+gula+garam+cajun, celup telur, lalu tepung panir. Telur bisa diganti air, lapis beberapa kali lagi terigunya.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdQS6PbUYAEoVIg.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdQS7l5VcAAjYOf.jpg" alt="">
