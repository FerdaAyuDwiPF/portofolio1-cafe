          <h1>2. Sushi. </h1>
          <p>Jika baru pertama kali bikin sushi, jangan terlalu berani nuangin cuka. Lebih baik terlalu sedikit karena bisa ditambah, kalau kebanyakan jadi fatal. Takarannya 4 centong kayu nasi = 1/2 sdt cuka. Jangan sedikit-sedikit pas nyobain nasinya ya, karena tidak akan kerasa kalo cukanya kebanyakan.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDISjbUEAEXFFC.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIS6GUcAAF3ph.jpg" alt="">
          <h1>3. Bubur Sumsum. </h1>
          <p>Memakai santan perasan pertama. Harus sabar mengaduk terus selama masak biar santan tidak pecah. Saat mendidih keliatannya memang encer, maka dari itu harus diresting sebelum disajikan. Kalau tidak mengental juga, artinya santannya encer.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDITl1UYAARljN.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIUp6UcAQ5dtp.jpg" alt="">
          <h1>4. Brownies Kukus. </h1>
          <p>Karena membuatnya tidak memakai mixer, jadi mengaduknya satu per satu. Pertama Telur + gula dulu, aduk. Kemudian Susu, aduk. Lalu Tepung, aduk. Selanjutnya Vanilla, baking powder, baking soda, garam, aduk. Terakhir Coklat tim masuk, aduk lagi biar rata. Jangan lupa sering cek air kukusan agarr tidak sampai kering.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIVRgUMAA2Jy_.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIV6bUYAA794d.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIWmKVAAAlw99.jpg" alt="">
          <h1>5. Sambal Matah. </h1>
          <p>Sambal matah kurang sedap tanpa minyak kelapa dan terasi. Semua bahan terutama sereh harus potong setipis mungkin.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIXwOVAAAomjV.jpg" alt="">
          <h1>6. Kaldu Ayam. </h1>
          <p>Kuncinya adalah sebelum air rebusan masuk untuk buat kaldu ayamnya, tumis bawang putih yang banyak dengan mentega sampai agak kering atau gosong. Baru air masuk, lalu diaduk.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIYjWVAAE_sxh.jpg" alt="">
          <h1>7. Chewy Choco Cookies. </h1>
          <p>Masukkan cokelat masak yang dipotong dadu kasar dan choco chips ke kulkas dulu baru dicampur ke adonan, agar cokelat tidak meleleh dalam proses pengadukan. Nanti adonannya jadi warna cokelat bukannya keemasan. Adonannya pakai tepung protein rendah ya.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIaKfU0AA2Xzb.jpg" alt="">
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdDIa4PUwAEb7J6.jpg" alt="">
