          <h1>1. Cireng. </h1>
          <p>Adukan cireng biang harus masih panas-panasnya saat dimasukkan ke tepung tapioka yang masih kering. Saat sudah bergumpal dan dibentuk, resting, masuk kulkas dulu, baru goreng.</p>
