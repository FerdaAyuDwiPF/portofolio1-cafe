          <h1>21. Bistik Lidah Sapi. </h1>
          <p>Resepnya dipisah-pisah stepnya (1)Bikin bawang+kecap+mentega, (2)Masukkan lidah sampai empuk, (3)Aduk dengan semua rempah-rempah, (4)Baru campur tomat dan tunggu sampai agak sat.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EhtrnsdVkAQXDdq.jpg" alt="">
