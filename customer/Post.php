          <h1>14. Red Velvet. </h1>
          <p>Red velvet dibuat dengan cara dikukus. Masaknya pun ribet berlayer dan beragam adonan, pantesan mahal. Tipsnya yaitu kalau tidak pake mixer tetep ayak tepung saat masuk ke adonan cair secara terus-menerus, dan bergantian masuknya. Kukusan harus sudah mendidih sebelum adonan masuk.</p>
