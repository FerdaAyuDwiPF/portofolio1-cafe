          <h1>19. Donat. </h1>
          <p>Tips agar tidak perlu diulen. Pisah adonan ragi+gula, siapin adonan kering, kemudian siapin adonan butter+telur. Barulah campur ketiganya, diamkan selama 2 jam. Hasilnya akan lembut!</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/Ef_ebJiUMAEgXxb.jpg" alt="">
