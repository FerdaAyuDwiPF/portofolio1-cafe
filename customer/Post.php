          <h1>11. Crispy Cookies. </h1>
          <p>Adonan cookie kering ini beda bgt sm chewy cookie yg ada di atas. Aku anaknya mager jd butter kulelehin di bwh matahari lalu diaduk dgn bahan lain tanpa mixer. Lagi2, kunci mengaduk tanpa mixer adalah bahan masuk bergantian dan menyiapkan power utk mengaduk.</p>
          <img class="img-fluid" src="https://pbs.twimg.com/media/EdgN5P7VcAgi1ZF.jpg" alt="">
