          <h1>16. Ice Cream. </h1>
          <p>Cuma memakai 3 bahan, pas mengaduk whipped cream jangan dicampur apa-apa dulu sampai jadi foam. Kalau pakai pasta perasa dihitung proporsinya sesuai adonan (Biasanya ada keterangan di botolnya). Eum Seger~ </p>
